<?php

namespace Dojo\Morse;

class Convertor
{

    //const MORSE =

    public static function controller($code){



        $morse = [
            "-.-.--" => "!",
            "....-" => "4",
            "..--.." => "?",
            "--..--" => ",",
            ".-.-.-" => ".",
            "..---" => "2",
            "...--" => "3",
            "--..." => "7",
            "-...." => "6",
            "....." => "5",
            "---.." => "8",
            "-..." => "B",
            "----." => "9",
            ".--." => "P",
            "-----" => "0",
            "--.." => "Z",
            "-.--" => "Y",
            "-..-" => "X",
            "-.-." => "C",
            "...-" => "V",
            ".----" => "1",
            "..-." => "F",
            "...." => "H",
            ".---" => "J",
            "--.-" => "Q",
            "-..-." => "/",
            ".-.." => "L",
            "..." => "S",
            "---" => "O",
            "-.-" => "K",
            ".-." => "R",
            "..-" => "U",
            "-.." => "D",
            ".--" => "W",
            "--." => "G",
            "-." => "N",
            "--" => "M",
            ".." => "I",
            ".-" => "A",
            "-" => "T",
            "." => "E",
        ];



        $letter = "";
        for ($i=0 ; $i<strlen($code) ; $i++) {
            if ($code[$i] !== " ") {
                $letter.=$code[$i];
            }
            else {
                if ($i === strlen($code)-1){
                    $letter.=$code[$i];
                }
            }
        }
        return $morse[$letter];


    }
}

